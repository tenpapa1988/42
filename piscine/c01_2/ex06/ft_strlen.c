#include <stdio.h>

int	ft_strlen(char *str);

int	main(void)
{
	char	str[]="aseedas1";

	ft_strlen(str);
	printf("%d\n", ft_strlen(str));

	return (0);
}

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}
