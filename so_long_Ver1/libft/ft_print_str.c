#include "ft_printf.h"

void	ft_print_str(char *c, int *len)
{
	int	i;

	i = 0;
	if (!c)
	{
		write(1, "(null)", 6);
		*len += 6;
		return ;
	}
	while (c[i] != '\0')
	{
		write(1, &c[i], 1);
		(*len)++;
		i++;
	}
}
